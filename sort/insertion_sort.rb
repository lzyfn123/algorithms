# 插入排序（Insertion Sort）
# [86, 97, 65, 16, 43, 44, 56, 71, 91, 70, 16]
# [86, 97, 65, 16, 43, 44, 56, 71, 91, 70, 16]
# [65, 86, 97, 16, 43, 44, 56, 71, 91, 70, 16]
# [16, 65, 86, 97, 43, 44, 56, 71, 91, 70, 16]
# [16, 43, 65, 86, 97, 44, 56, 71, 91, 70, 16]
# [16, 43, 44, 65, 86, 97, 56, 71, 91, 70, 16]
# [16, 43, 44, 56, 65, 86, 97, 71, 91, 70, 16]
# [16, 43, 44, 56, 65, 71, 86, 97, 91, 70, 16]
# [16, 43, 44, 56, 65, 71, 86, 91, 97, 70, 16]
# [16, 43, 44, 56, 65, 70, 71, 86, 91, 97, 16]

def insertion_sort(arr)
  step = arr.size - 1
  
  1.upto(step).each do |i|
    current = arr[i]
    index   = i
    
    (i-1).downto(0).each do |j|
      break if arr[j] < current
      
      arr[j], arr[j+1] = arr[j+1], arr[j]
      index            = j
    end
    
    arr[index] = current
  end
  
  arr
end

