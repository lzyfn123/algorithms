# 冒泡排序（Bubble Sort）
# [86, 97, 65, 16, 43, 44, 56, 71, 91, 70, 16]
# [86, 97, 65, 16, 43, 44, 56, 71, 91, 70, 16]
# [16, 97, 86, 65, 43, 44, 56, 71, 91, 70, 16]
# [16, 16, 97, 86, 65, 44, 56, 71, 91, 70, 43]
# [16, 16, 43, 97, 86, 65, 56, 71, 91, 70, 44]
# [16, 16, 43, 44, 97, 86, 65, 71, 91, 70, 56]
# [16, 16, 43, 44, 56, 97, 86, 71, 91, 70, 65]
# [16, 16, 43, 44, 56, 65, 97, 86, 91, 71, 70]
# [16, 16, 43, 44, 56, 65, 70, 97, 91, 86, 71]
# [16, 16, 43, 44, 56, 65, 70, 71, 97, 91, 86]
# [16, 16, 43, 44, 56, 65, 70, 71, 86, 97, 91]
# [16, 16, 43, 44, 56, 65, 70, 71, 86, 91, 97]
# [16, 16, 43, 44, 56, 65, 70, 71, 86, 91, 97]

def bubble_sort(arr = [])
  step = arr.size - 1
  
  0.upto(step).each do |i|
    (i + 1).upto(step).each do |j|
      arr[i], arr[j] = arr[j], arr[i] if arr[i] > arr[j]
    end
  end
  
  arr
end

arr = [86, 97, 65, 16, 43, 44, 56, 71, 91, 70, 16]
p bubble_sort(arr)
